    //--------------------- lexer sintactico -----------------------------------
    
     public void probarLexerFile() throws IOException
     {
        Reader reader = new BufferedReader(new FileReader("codigo.txt"));
        LexerSintax lexer = new LexerSintax (reader);
        while(true)
        {
            SintacticTokens token = lexer.yylex();
            if(token==null)
            {
                ResultSintax.setText(ResultSintax.getText()+"Analisis realizado correctamente"+"\n");
                //System.out.println("EOF");
                return;
            }
            
            switch(token)
            {
                case Digito:
                    ResultSintax.setText(ResultSintax.getText()+"Token Numeros:" +lexer.lexeme+"\n");
                    //System.out.println("TOKEN: "+token+" "+lexer.lexeme);
                break;
                     case String:
                    ResultSintax.setText(ResultSintax.getText()+"Token Palabras:"+lexer.lexeme+"\n");
                    //System.out.println("TOKEN: "+token+" "+lexer.lexeme);
                break;
                    
                    
                    
                default:
                    ResultSintax.setText(ResultSintax.getText()+"SINTACTICO: "+token+"\n");
                    //System.out.println("TOKEN: "+token);
            }
        }
    }
     
