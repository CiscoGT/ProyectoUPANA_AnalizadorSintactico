    //---------------------- lexer LEXICO ------------------------------------------- 
    public void probarLexer() throws IOException
    {
        review  c= new review ();
        
        File fichero = new File("fichero.txt");
        PrintWriter Writer;
           
        
        try 
        {
            Writer = new PrintWriter(fichero);
               Writer.print(abrir.getText());
                  Writer.close();
            
        } 
        
        catch(FileNotFoundException ex)
        {
           // Logger.getLogger(InterfazCompilador.class.getName()).log(Level.SEVERE,null,ex);
            
        }
        
        Reader reader;
        reader = new BufferedReader(new FileReader("fichero.txt"));
        LexerFlex lexer = new LexerFlex(reader);
        
        String Resultado="";
        
        while(true)
        {
            Token tok= lexer.yylex();
            
            if (tok == null) 
            {
                
                Resultado = Resultado+"FIN";
                ResultadoLexic.setText(Resultado);
                return ;
            }
            
            switch(tok)
            {
                case ERROR:
                    Resultado = Resultado+"ERROR LEXICO: "+lexer.lexeme+" "+" "+" Linea: "
                            + c.linea+"\n";
                    break;
                case CADENA: case NUM:
                    Resultado = Resultado+"LEXEMA: "+tok+" "+" "+" "+" "+" TOKENS: "+lexer.lexeme+"\n";
                    break;
                default: 
                    Resultado = Resultado+"LEXEMA: "+tok+" "+" "+" "+" "+" TOKENS: "+lexer.lexeme+"\n";
                    break;
            }
                    
        } 
    } 
    
    
